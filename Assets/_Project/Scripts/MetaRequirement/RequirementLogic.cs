namespace ProjectC.MetaRequirement
{
    /// <summary>
    /// Логика проверки наличия требуемых предметов в инвентаре игрока.
    /// </summary>
    public enum RequirementLogic
    {
        /// <summary>ВСЕ предметы из списка должны быть у игрока.
        /// Пустой список → trivially true (нет требований).</summary>
        All = 0,

        /// <summary>ХОТЯ БЫ ОДИН предмет из списка должен быть у игрока.
        /// Пустой список → false (нечего проверить).</summary>
        Any = 1,

        /// <summary>Как минимум <see cref="MetaRequirement._requiredCount"/> РАЗНЫХ предметов
        /// из списка должны быть у игрока. Например: 3 из 5 фрагментов карты.
        /// _requiredCount &lt; 1 → считается как 1.</summary>
        AtLeastN = 2,
    }
}
